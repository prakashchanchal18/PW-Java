package physicsWallah.Revision1.BasicsOfJava;

public class ArithmeticOperationOnDouble {
    public static void main(String[] args) {
        double x = 22;
        double y = 7;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}
