package physicsWallah.Revision1.BasicsOfJava;

public class ArithmeticOperationOnInt {
    public static void main(String[] args) {
        int x = 5;
        int y = 20;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y); // forward slash (/) backward slash (\)

    }
}
