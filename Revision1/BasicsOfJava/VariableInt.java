package physicsWallah.Revision1.BasicsOfJava;

public class VariableInt {
    public static void main(String[] args) {
        // Variables and their declaration.
        // 1. Variables as containers.
        // 1.1 int variable. (Means its value can change)
        // 1.2 int - 4 bytes (1 GB = 1000 MB -- 1 MB = 1000 KB -- 1 KB = 1000 Bytes)
        int x = 5;
        System.out.println(x);
        x = 400; // x is been updated.
        System.out.println(x);
        System.out.println(x+1);
        x = x + 5;
        System.out.println(x);
    }
}
